function togglepopupspeaker1() {
    var dialog = document.getElementById("dialog");
    dialog.classList.toggle("active");

    if (dialog.classList.contains("active")) {
        dialog.showModal();

        document.body.style.overflow = 'hidden';
    }
    else {
        dialog.close();

        document.body.style.overflow = '';
    }
}
